import { Schema, model } from "mongoose";

const schema = Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Admin =model("Admin",schema);
export default Admin;